package org.crazyit.drools;

public class SyntaxPerson2 {

	private String name;
	
	private int age;
	

	public SyntaxPerson2(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
